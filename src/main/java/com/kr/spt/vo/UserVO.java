package com.kr.spt.vo;

public class UserVO {

		private String id;
		private String user_name;
		private String user_pass;
		private String user_phone;
		private String user_addr;
		private String user_issys;
		private String user_pushKey;
		private String user_intidate;
		
		public String getId() {
			return id;
		}
		public void setId(String id) {
			this.id = id;
		}
		public String getUser_name() {
			return user_name;
		}
		public void setUser_name(String user_name) {
			this.user_name = user_name;
		}
		public String getUser_pass() {
			return user_pass;
		}
		public void setUser_pass(String user_pass) {
			this.user_pass = user_pass;
		}
		public String getUser_phone() {
			return user_phone;
		}
		public void setUser_phone(String user_phone) {
			this.user_phone = user_phone;
		}
		public String getUser_addr() {
			return user_addr;
		}
		public void setUser_addr(String user_addr) {
			this.user_addr = user_addr;
		}
		public String getUser_issys() {
			return user_issys;
		}
		public void setUser_issys(String user_issys) {
			this.user_issys = user_issys;
		}
		public String getUser_pushKey() {
			return user_pushKey;
		}
		public void setUser_pushKey(String user_pushKey) {
			this.user_pushKey = user_pushKey;
		}
		public String getUser_intidate() {
			return user_intidate;
		}
		public void setUser_intidate(String user_intidate) {
			this.user_intidate = user_intidate;
		}

		@Override
		public String toString() {
			return "UserVO [id=" + id + ", user_name=" + user_name
					+ ", user_pass=" + user_pass + ", user_phone=" + user_phone
					+ ", user_addr=" + user_addr + ", user_issys=" + user_issys
					+ ", user_pushKey=" + user_pushKey + ", user_intidate="
					+ user_intidate + "]";
		}
		
}
